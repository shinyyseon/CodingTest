class Solution {
    public String solution(String[] cards1, String[] cards2, String[] goal) {
        int i = 0;
        int j = 0;
        
        for(String item : goal) {
            if(i < cards1.length && item.equals(cards1[i])) 
                i++;
            else if(j < cards2.length && item.equals(cards2[j])) 
                j++;
            else 
                return "No";
        }
        return "Yes";
    }
}